import cv2
import imutils
data = cv2.imread("/home/pi/image2.jpg")
resized = imutils.resize(data,width=600)
data_gray = cv2.cvtColor(resized,cv2.COLOR_BGR2GRAY)
rotated = imutils.rotate_bound(resized,-45)
blurred_1 = cv2.GaussianBlur(resized,(11,11),0)
blurred_2 = cv2.GaussianBlur(resized,(31,31),0)
output_1 = resized.copy()
output_2 = resized.copy()
output_3 = resized.copy()
output_4 = resized.copy()
cv2.rectangle(output_1,(5,10),(500,500),(0,0,255),2)
cv2.circle(output_2,(150,150),50,(255,0,0),10)
cv2.line(output_3,(5,10),(400,350),(0,255,0),2)
cv2.putText(output_4,"hanhan hello OPencv",(30,300),cv2.FONT_HERSHEY_SIMPLEX,1,(255,0,0),1)
cv2.imshow("RESIZED",resized)
cv2.imshow("color:GRAY",data_gray)
cv2.imshow("ROTATA_BOUND",rotated)
cv2.imshow("GaussianBlur",blurred_1)
cv2.imshow("GaussianBlur_2",blurred_2)
cv2.imshow("rectangle",output_1)
cv2.imshow("circle",output_2)
cv2.imshow("line",output_3)
cv2.imshow("Textput",output_4)
cv2.waitKey(0)